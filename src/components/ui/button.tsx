// src/components/StandMap.tsx
import { MapContainer, TileLayer, Marker, Popup } from 'react-leaflet';
import { Icon } from 'leaflet';
import 'leaflet/dist/leaflet.css';
import { Stand } from '@/types';

interface StandMapProps {
  stands: Stand[];
  onStandSelect?: (stand: Stand) => void;
  center?: [number, number];
  zoom?: number;
  showRoute?: boolean;
}

export function StandMap({
  stands,
  onStandSelect,
  center = [51.1657, 10.4515],
  zoom = 6,
  showRoute = false
}: StandMapProps) {
  const customIcon = new Icon({
    iconUrl: '/marker-icon.png',
    iconSize: [25, 41],
    iconAnchor: [12, 41],
    popupAnchor: [1, -34],
  });

  return (
    <MapContainer
      center={center}
      zoom={zoom}
      className="h-full min-h-[400px] rounded-lg"
    >
      <TileLayer
        attribution='&copy; <a href="https://www.openstreetmap.org/copyright">OpenStreetMap</a> contributors'
        url="https://{s}.tile.openstreetmap.org/{z}/{x}/{y}.png"
      />
      {stands.map((stand) => (
        <Marker
          key={stand.id}
          position={[stand.location.latitude, stand.location.longitude]}
          icon={customIcon}
          eventHandlers={{
            click: () => onStandSelect?.(stand),
          }}
        >
          <Popup>
            <div className="p-2">
              <h3 className="font-medium">{stand.name}</h3>
              <p className="text-sm text-gray-600">{stand.location.address}</p>
              {stand.currentPoster && (
                <p className="text-sm mt-1">
                  <span className="font-medium">Aktuelles Plakat:</span>{' '}
                  {stand.currentPoster}
                </p>
              )}
            </div>
          </Popup>
        </Marker>
      ))}
    </MapContainer>
  );
}
